# include < iostream >
# include < cmath >
using namespace std;
int main()
{
	// power 

	cout << " 5^2 = ";
	cout << pow(5, 2) << endl;





	// square root 

	cout << "√ (9) = ";
	cout << sqrt(9) << endl;





	// ceil
	
	cout << "ceil (9.01) = ";
	cout << ceil(9.01) << endl;





	// floor

	cout << "floor (9.99) = ";
	cout << floor(9.99) << endl;





	// Trigonometric

	cout << "cos (0) = ";
	cout << cos(0) << endl;


	cout << "sin (0) = ";
	cout << sin(0) << endl;


	cout << "tan (0) = ";
	cout << tan(0) << endl;





	// max

	cout << "max(15,17) = ";
	cout << max(15, 17) << endl;





    // min

	cout << "min(4,9) = ";
	cout << min(4, 9) << endl;





	// e = 2.718

	cout << "exp(2) = ";
	cout << exp(2) << endl;





	// 1.1 % 2.2 = error

	cout << "fmod(1.1,2.2) = ";
	cout << fmod(1.1, 2.2) << endl;
	
	
	
	

	// fabs 

	cout << "fabs (-1) = ";
	cout << fabs(-1) << endl;






	return 0;


}
